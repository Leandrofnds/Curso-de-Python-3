#Aspas simples
print('Leandro Fernandes')

#Asps duplas 
print("Leandro Fernandes")

#Escape, serve para ignorar o proximo caractere depois do \ 
print("Leandro \"Fernandes\"")

# r serve para considerar o \ dentro do escape, geralmente utilizado para expressoes regulares
print(r"Leandro \"Fernandes\"")

